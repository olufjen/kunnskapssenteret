package no.helsebiblioteket.admin.domain;

public class Url {
	private String value;
	
	public Url() {
		super();
	}

	public Url(String value) {
		super();
		this.value = value;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
}
